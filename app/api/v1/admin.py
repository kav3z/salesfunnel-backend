# Admin endpoints
# GET /api/v1/admin/dashboard - Get system-wide stats
# GET /api/v1/admin/users - List all users (with filters/search)
# GET /api/v1/admin/users/{id} - Get user details
# PATCH /api/v1/admin/users/{id}/status - Activate/deactivate user
# DELETE /api/v1/admin/users/{id} - Delete user
# GET /api/v1/admin/orders - List all orders (with filters)
# GET /api/v1/admin/orders/{id} - Get order details
# PATCH /api/v1/admin/orders/{id}/override - Manual intervention for disputes
# GET /api/v1/admin/payments - List all payments
# GET /api/v1/admin/payments/{id} - Get payment details
# PATCH /api/v1/admin/payments/{id}/override - Manual payment approval/rejection
# GET /api/v1/admin/audit-logs - View audit trail
# GET /api/v1/admin/analytics/orders - Order analytics
# GET /api/v1/admin/analytics/payments - Payment analytics
# GET /api/v1/admin/analytics/fulfillment - Fulfillment time analytics
