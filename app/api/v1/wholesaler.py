# Wholesaler endpoints
# GET /api/v1/wholesaler/distributors - List available distributors
# GET /api/v1/wholesaler/distributors/{id} - Get distributor details
# GET /api/v1/wholesaler/products - List products (filterable by distributor)
# GET /api/v1/wholesaler/products/{id} - Get product details
# POST /api/v1/wholesaler/orders - Create new order
# GET /api/v1/wholesaler/orders - List my orders
# GET /api/v1/wholesaler/orders/{id} - Get order details
# POST /api/v1/wholesaler/orders/{id}/payment - Submit payment info
# POST /api/v1/wholesaler/orders/{id}/payment-proof - Upload payment proof
# GET /api/v1/wholesaler/notifications - Get my notifications
# PATCH /api/v1/wholesaler/notifications/{id}/read - Mark notification as read
# GET /api/v1/wholesaler/profile - Get my profile
# PUT /api/v1/wholesaler/profile - Update my profile
